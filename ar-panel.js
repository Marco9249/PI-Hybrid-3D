function openArPanel(chId) {
    // Play transition sound
    if (window.soundManager) {
        window.soundManager.playTransition();
        window.soundManager.playClick();
    }

    const data = chapterData[chId];
    if (!data) return;

    const inner = document.getElementById('ar-panel-inner');
    inner.innerHTML = `
        <div class="ar-ch-num" style="color:${data.color}">${data.tag}</div>
        <div class="ar-ch-title">${data.title}</div>
        <div class="ar-divider"></div>
        <div class="ar-body">${data.body}</div>
    `;

    document.getElementById('ar-panel').classList.add('open');
    document.getElementById('ar-panel-overlay').classList.add('open');
    document.body.style.overflow = 'hidden';

    // Professional Equation Rendering
    if (window.renderMathInElement) {
        renderMathInElement(inner, {
            delimiters: [
                { left: '$$', right: '$$', display: true },
                { left: '$', right: '$', display: false },
                { left: '\\(', right: '\\)', display: false },
                { left: '\\[', right: '\\]', display: true }
            ],
            throwOnError: false
        });
    }
}

function closeArPanel() {
    if (window.soundManager) window.soundManager.playClick();
    document.getElementById('ar-panel').classList.remove('open');
    document.getElementById('ar-panel-overlay').classList.remove('open');
    document.body.style.overflow = '';
}

// Close on Escape key
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closeArPanel();
});
